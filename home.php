                               100 is a significant number because it refers to a report released by Guria in 2014 entitled 100 Cases of Police Misconduct in Sex Trafficking, Child Prostitution, and Rape. We hope the worldwide mobilization of 100 Schools for 100 Cases will demand that the world notice the atrocities in these 100 cases and advocate for real action to be taken against the police misconduct that allows trafficking to flourish.

